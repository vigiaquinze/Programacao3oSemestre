# Aritméticos

#Exemplos:

a = 10
b = 3

soma = a + b
subtracao = a - b
multiplicacao = a * b
divisao = a / b
divisao_inteira = a // b
resto = a % b
potencia = a ** b

print(soma, subtracao, multiplicacao, divisao, divisao_inteira, resto, potencia)

# Relacionais

#Exemplos:

x = 5
y = 10

igual = x == y
diferente = x != y
menor = x < y
maior = x > y
menor_igual = x <= y
maior_igual = x >= y

print(igual, diferente, menor, maior, menor_igual, maior_igual)

# Lógicos

#Exemplos:

p = True
q = False

resultado_and = p and q
resultado_or = p or q
resultado_not = not p

print(resultado_and, resultado_or, resultado_not)
